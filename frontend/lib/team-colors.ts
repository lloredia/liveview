/**
 * Get the brand color for a team, falling back to undefined if unknown.
 */
export function getTeamColor(teamName: string): string | undefined {
  return TEAM_COLORS[teamName];
}

/**
 * Build a subtle horizontal gradient string for a match card.
 * Left = home team color at given opacity, right = away team color.
 * Middle fades to transparent for a clean look.
 */
export function buildTeamGradient(
  homeTeamName: string,
  awayTeamName: string,
  opacity = 0.08,
): string | undefined {
  const homeColor = getTeamColor(homeTeamName);
  const awayColor = getTeamColor(awayTeamName);
  if (!homeColor && !awayColor) return undefined;

  const toRgba = (hex: string, a: number) => {
    const r = parseInt(hex.slice(1, 3), 16);
    const g = parseInt(hex.slice(3, 5), 16);
    const b = parseInt(hex.slice(5, 7), 16);
    return `rgba(${r},${g},${b},${a})`;
  };

  const left = homeColor ? toRgba(homeColor, opacity) : "transparent";
  const right = awayColor ? toRgba(awayColor, opacity) : "transparent";

  return `linear-gradient(to right, ${left} 0%, transparent 40%, transparent 60%, ${right} 100%)`;
}
